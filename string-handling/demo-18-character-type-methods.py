print('abc123'.isalnum())#True --> checks if it contains alphabets and digits
print('abc'.isalpha())#True --> checks if it contains only alphabets
print('abc123'.isalpha())#False --> checks if it contains only alphabets
print('123'.isdigit())#True --> checks if it contains only digits
print('Good Morning'.isupper())#False --> checks if it contains only upper case
print('Good Morning'.islower())#False --> checks if it contains only lower case
print('Good Morning'.istitle())#True --> checks if it contains only title case
print('Good Morning'.isspace())#False --> checks if it contains only spaces
